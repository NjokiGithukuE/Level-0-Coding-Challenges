function celciusToFahrenheit(celcius){
    const result = (celcius * 1.8)+32; //Convert celcius to fahrenheit
    return result;
  }
  celciusToFahrenheit(43);
  
  function fahrenheitToCelcius(fahrenheit){
    const result1 = (fahrenheit - 32)/1.8;
    return result1;
  }
  fahrenheitToCelcius(65);